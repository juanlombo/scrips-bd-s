-- 									-- Revisar tamaño bd
======================================================================================================================
SELECT 
    table_schema AS 'Base de Datos',
    ROUND(SUM(data_length + index_length) / 1024 / 1024 / 1024, 2) AS 'Tamaño (GB)'
FROM 
    information_schema.tables
GROUP BY 
    table_schema
ORDER BY 
    'Tamaño (GB)' DESC;

